# Definig a variable called “name” and assign to “Abdi” as value
name = "Abdi"

'''
    This is how we define a variable in Python and give it a value.
    - We first state the variable name
    - Then an equal sign
    - And then the value that we want to assign it.
'''

# Here if we want to print the name, all we have to do is call print and then inside brackets, and put our variable 
print(name)

'''
    If we run this program, we’ll see that “Abdi” is printed out because that is how we programmed.
'''

'''
    Instead of printing the variable name, we can print “Abdi” directly inside the print statement, 
    which is fine, it means exactly the same thing. Because “name” is the variable name, 
    which we used it inside the print statement to print the value out.
'''

print("Abdi")

